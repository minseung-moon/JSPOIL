package DTO;

public class OilDTO {
	private String o_number;
	private String olikind;
	private String price;
	public String getO_number() {
		return o_number;
	}
	public void setO_number(String o_number) {
		this.o_number = o_number;
	}
	public String getOlikind() {
		return olikind;
	}
	public void setOlikind(String olikind) {
		this.olikind = olikind;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	
	
}
